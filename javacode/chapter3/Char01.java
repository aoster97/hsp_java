public class Char01{	
 	public static void main(String[] args){
		char c1 = 'a';
		char c2 = '\t';
		char c3 = '韩';
		char c4 = 97; //说明：字符类型可以直接存放一个数字
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		System.out.println(c4);//当输出c4的时候,会输出97代表的字符 = >编码
		//sublime快捷键的使用
		//将首选项中按键绑定的设置 移动到 用户绑定中
		//
	}
}